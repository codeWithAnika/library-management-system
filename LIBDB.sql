CREATE OR REPLACE TRIGGER fine_trigger
BEFORE UPDATE ON BORROWING
FOR EACH ROW
BEGIN
    IF :NEW.Return_Date IS NOT NULL AND :NEW.Return_Date > :NEW.Due_Date THEN
