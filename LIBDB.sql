CREATE TABLE LIB_USER (
    User_ID NUMBER PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL,
    Email VARCHAR2(100) NOT NULL UNIQUE,
    Role VARCHAR2(20) NOT NULL,
    Department VARCHAR2(100) DEFAULT 'General',

    CHECK (Role IN ('Student','Faculty','Staff'))
);

CREATE TABLE AUTHOR (
    Author_ID NUMBER PRIMARY KEY,
    Author_Name VARCHAR2(100) NOT NULL
);

CREATE TABLE CATEGORY (
    Category_ID NUMBER PRIMARY KEY,
    Category_Name VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE PUBLISHER (
    Publisher_ID NUMBER PRIMARY KEY,
    Publisher_Name VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE BOOK (
    Book_ID NUMBER PRIMARY KEY,
    Title VARCHAR2(200) NOT NULL,
    ISBN VARCHAR2(20) NOT NULL UNIQUE,
    Type VARCHAR2(50) NOT NULL,
    Publication_Year NUMBER NOT NULL,
    Availability_Status VARCHAR2(20) DEFAULT 'Available',

    Category_ID NUMBER NOT NULL,
    Publisher_ID NUMBER NOT NULL,

    FOREIGN KEY (Category_ID)
        REFERENCES CATEGORY(Category_ID),

    FOREIGN KEY (Publisher_ID)
        REFERENCES PUBLISHER(Publisher_ID),

    CHECK (Publication_Year >= 1500 AND Publication_Year <= EXTRACT(YEAR FROM SYSDATE)),
    CHECK (Availability_Status IN ('Available','Borrowed'))
);

CREATE TABLE BOOK (
    Book_ID NUMBER PRIMARY KEY,
    Title VARCHAR2(200) NOT NULL,
    ISBN VARCHAR2(20) NOT NULL UNIQUE,
    Type VARCHAR2(50) NOT NULL,
    Publication_Year NUMBER NOT NULL,
    Availability_Status VARCHAR2(20) DEFAULT 'Available',

    Category_ID NUMBER NOT NULL,
    Publisher_ID NUMBER NOT NULL,

    FOREIGN KEY (Category_ID)
        REFERENCES CATEGORY(Category_ID),

    FOREIGN KEY (Publisher_ID)
        REFERENCES PUBLISHER(Publisher_ID),

    CHECK (Publication_Year >= 1500 AND Publication_Year <= 2026),
    CHECK (Availability_Status IN ('Available','Borrowed'))
);

CREATE TABLE BORROWING (
    Borrowing_ID NUMBER PRIMARY KEY,
    User_ID NUMBER NOT NULL,
    Book_ID NUMBER NOT NULL,

    Issue_Date DATE NOT NULL,
    Due_Date DATE NOT NULL,
    Return_Date DATE,
    Fine_Amount NUMBER(10,2) DEFAULT 0,

    FOREIGN KEY (User_ID)
        REFERENCES LIB_USER(User_ID),

    FOREIGN KEY (Book_ID)
        REFERENCES BOOK(Book_ID),

    CHECK (Due_Date >= Issue_Date),
    CHECK (Fine_Amount >= 0)
);

CREATE TABLE RESERVATION (
    Reservation_ID NUMBER PRIMARY KEY,
    User_ID NUMBER NOT NULL,
    Book_ID NUMBER NOT NULL,

    Reservation_Date DATE DEFAULT SYSDATE,
    Status VARCHAR2(20) DEFAULT 'Pending',

    FOREIGN KEY (User_ID)
        REFERENCES LIB_USER(User_ID),

    FOREIGN KEY (Book_ID)
        REFERENCES BOOK(Book_ID),

    CHECK (Status IN ('Pending','Approved','Cancelled'))
);

CREATE TABLE WRITES (
    Author_ID NUMBER,
    Book_ID NUMBER,

    PRIMARY KEY (Author_ID, Book_ID),

    FOREIGN KEY (Author_ID)
        REFERENCES AUTHOR(Author_ID),

    FOREIGN KEY (Book_ID)
        REFERENCES BOOK(Book_ID)
);

